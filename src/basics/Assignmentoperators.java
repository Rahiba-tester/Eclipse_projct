package basics;

public class Assignmentoperators {

	public static void main(String[] args) {
	 int a=55;
	 System.out.println(a);
	 int b;
	 b=a;
	 System.out.println(b);
	 b+=a;  /* (b= b+a)  */
	 System.out.println(b);
	 b-=a;   /* (b=b-a)  */
	 System.out.println(b);
	  
	 b*=a;  
	 // b=b*a   
	 System.out.println(b);
	 b/=a;
	  System.out.println(b);
	 
	 
	}

}
